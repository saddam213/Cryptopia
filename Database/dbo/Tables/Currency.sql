CREATE TABLE [dbo].[Currency]
(
	[Id] INT IDENTITY (1, 1) NOT NULL,
    [Name] NVARCHAR(50) NOT NULL, 
    [Symbol] NVARCHAR(50) NOT NULL, 
	[AlgoTypeId] TINYINT NOT NULL,
	[NetworkType] TINYINT NOT NULL,
	[Balance] DECIMAL(38,8) NULL,
	[WalletUser] NVARCHAR(128) NOT NULL,
	[WalletPass] NVARCHAR(128) NOT NULL,
	[WalletPort] INT NOT NULL,
	[WalletHost] NVARCHAR(128) NOT NULL,
    [TradeFee] DECIMAL(38, 8) NOT NULL DEFAULT 0, 
	[MinBaseTrade] DECIMAL(38, 8) NOT NULL DEFAULT 0.00002000, 
	[MinTradeAmount] DECIMAL(38, 8) NOT NULL, 
	[MaxTradeAmount] DECIMAL(38, 8) NOT NULL, 
	[TxFee] DECIMAL(38, 8) NOT NULL DEFAULT 0, 
	[PoolFee] DECIMAL(38, 8) NOT NULL DEFAULT 0, 
	[WithdrawFee] DECIMAL(38, 8) NOT NULL DEFAULT 0, 
	[WithdrawFeeType] TINYINT NOT NULL DEFAULT(0),
	[MinWithdraw] DECIMAL(38, 8) NOT NULL, 
	[MaxWithdraw] DECIMAL(38, 8) NOT NULL, 
	[MinTip] DECIMAL(38, 8) NOT NULL, 
	[MinFaucet] DECIMAL(38, 8) NOT NULL, 
	[IsFaucetEnabled] BIT NOT NULL DEFAULT 0,
	[IsTipEnabled] BIT NOT NULL DEFAULT 0,
    [MinConfirmations] INT NOT NULL DEFAULT 6, 
	[Rank] INT NOT NULL, 
	[Status] TINYINT NOT NULL DEFAULT 0,
    [StatusMessage] NVARCHAR(1024) NULL, 
	[LastBlockHash] NVARCHAR(128) NULL,
	[ForumId] INT NOT NULL DEFAULT 0, 
	[BlockTime] INT NOT NULL DEFAULT 0, 
	[Block] INT NOT NULL DEFAULT 0, 
	[Summary] NVARCHAR(4000) NULL, 
	[Version] NVARCHAR(128) NULL, 
	[Connections] INT NOT NULL DEFAULT 0, 
	[Errors] NVARCHAR(4000) NULL, 
	[IsFeatured] BIT NOT NULL DEFAULT 0,
	[IsEnabled] BIT NOT NULL DEFAULT 0,
	CONSTRAINT [PK_Currency] PRIMARY KEY CLUSTERED ([Id] ASC) ,
	CONSTRAINT [FK_Currency_Algo] FOREIGN KEY ([AlgoTypeId]) REFERENCES [AlgoType]([Id]),
)
